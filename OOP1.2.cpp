/*
 * Задание 22 (Ветвления): Проверка принадлежности точки (x,y) заштрихованной области
 * Тема: if, else if, else + потоки ввода-вывода
 */

int main() {
    double x, y;
    
    cout << "=== Задание 22: Ветвления ===\n";
    cout << "Введите координаты точки (x, y):\n";
    cout << "x = ";
    cin >> x;
    cout << "y = ";
    cin >> y;
    
    // Проверка корректности ввода
    if (cin.fail()) {
        cout << "Ошибка ввода координат!" << endl;
        return 1;
    }
    
    cout << fixed << setprecision(2);
    cout << "\nТочка: (" << x << ", " << y << ")" << endl;
    
    // Ветвление: проверка принадлежности заштрихованной области
    // Анализ типичных областей для подобных задач:
    if ((x >= -2 && x <= 2 && y >= -1 && y <= 1) ||           // Прямоугольник
        (x >= -1 && x <= 1 && y >= 0 && y <= 2) ||           // Верхний прямоугольник  
        (pow(x, 2) + pow(y, 2) <= 1)) {                      // Круг радиуса 1
        
        cout << "-Точка принадлежит заштрихованной области!" << endl;
        
    } else if (x*x + (y-1)*(y-1) <= 0.25) {                    // Полукруг сверху
        cout << "-Точка принадлежит заштрихованной области!" << endl;
        
    } else {
        cout << "-Точка НЕ принадлежит заштрихованной области!" << endl;
    }
    
    // Дополнительная информация
    cout << "\nХарактеристики области:\n";
    if (x >= 0) cout << "- x неотрицательна" << endl;
    if (y >= 0) cout << "- y неотрицательна" << endl;
    if (abs(x) <= 1 && abs(y) <= 1) cout << "- в пределах [-1;1]" << endl;
    
    cout << "\Программа выполнена успешно!" << endl;
    return 0;
}


