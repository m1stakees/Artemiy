def gcd(a, b):
    """
    –†–µ–∫—É—Ä—Å–∏–≤–Ω–∞—è —Ñ—É–Ω–∫—Ü–∏—è –¥–ª—è –≤—ã—á–∏—Å–ª–µ–Ω–∏—è –Ω–∞–∏–±–æ–ª—å—à–µ–≥–æ –æ–±—â–µ–≥–æ –¥–µ–ª–∏—Ç–µ–ª—è (–ù–û–î)
    –¥–≤—É—Ö —Ü–µ–ª—ã—Ö —á–∏—Å–µ–ª —Å –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ–º –∞–ª–≥–æ—Ä–∏—Ç–º–∞ –ï–≤–∫–ª–∏–¥–∞.
    
    –ê–ª–≥–æ—Ä–∏—Ç–º –æ—Å–Ω–æ–≤–∞–Ω –Ω–∞ –º–∞—Ç–µ–º–∞—Ç–∏—á–µ—Å–∫–æ–º —Å–≤–æ–π—Å—Ç–≤–µ:
    –ù–û–î(a, b) = –ù–û–î(b, a mod b), –≥–¥–µ a mod b - –æ—Å—Ç–∞—Ç–æ–∫ –æ—Ç –¥–µ–ª–µ–Ω–∏—è a –Ω–∞ b
    
    Args:
        a (int): –ü–µ—Ä–≤–æ–µ —á–∏—Å–ª–æ
        b (int): –í—Ç–æ—Ä–æ–µ —á–∏—Å–ª–æ
        
    Returns:
        int: –ù–∞–∏–±–æ–ª—å—à–∏–π –æ–±—â–∏–π –¥–µ–ª–∏—Ç–µ–ª—å —á–∏—Å–µ–ª a –∏ b
    """
    
    # –ë–ê–ó–û–í–´–ô –°–õ–£–ß–ê–ô –†–ï–ö–£–†–°–ò–ò
    # –ï—Å–ª–∏ –≤—Ç–æ—Ä–æ–µ —á–∏—Å–ª–æ b —Ä–∞–≤–Ω–æ 0, —ç—Ç–æ –æ–∑–Ω–∞—á–∞–µ—Ç, —á—Ç–æ –º—ã –¥–æ—Å—Ç–∏–≥–ª–∏ —Ç–æ—á–∫–∏,
    # –≥–¥–µ –æ–¥–Ω–æ —á–∏—Å–ª–æ –¥–µ–ª–∏—Ç—Å—è –Ω–∞ –¥—Ä—É–≥–æ–µ –±–µ–∑ –æ—Å—Ç–∞—Ç–∫–∞.
    # –í —ç—Ç–æ–º —Å–ª—É—á–∞–µ –ø–µ—Ä–≤–æ–µ —á–∏—Å–ª–æ a –∏ —è–≤–ª—è–µ—Ç—Å—è –Ω–∞–∏–±–æ–ª—å—à–∏–º –æ–±—â–∏–º –¥–µ–ª–∏—Ç–µ–ª–µ–º.
    if b == 0:
        print(f"üéØ –î–æ—Å—Ç–∏–≥–Ω—É—Ç –±–∞–∑–æ–≤—ã–π —Å–ª—É—á–∞–π: b = 0, –≤–æ–∑–≤—Ä–∞—â–∞–µ–º –ù–û–î = {a}")
        return a
    
    # –í—ã—á–∏—Å–ª—è–µ–º –æ—Å—Ç–∞—Ç–æ–∫ –æ—Ç –¥–µ–ª–µ–Ω–∏—è a –Ω–∞ b –¥–ª—è –æ—Ç–ª–∞–¥–∫–∏ –∏ –ø–æ–Ω–∏–º–∞–Ω–∏—è –ø—Ä–æ—Ü–µ—Å—Å–∞
    remainder = a % b
    print(f"üîÑ –†–µ–∫—É—Ä—Å–∏–≤–Ω—ã–π –≤—ã–∑–æ–≤: –ù–û–î({a}, {b}) ‚Üí –ù–û–î({b}, {remainder})")
    
    # –†–ï–ö–£–†–°–ò–í–ù–´–ô –°–õ–£–ß–ê–ô
    # –í—ã–∑—ã–≤–∞–µ–º —ç—Ç—É –∂–µ —Ñ—É–Ω–∫—Ü–∏—é —Å –Ω–æ–≤—ã–º–∏ –∞—Ä–≥—É–º–µ–Ω—Ç–∞–º–∏:
    # - –ø–µ—Ä–≤—ã–π –∞—Ä–≥—É–º–µ–Ω—Ç: –ø—Ä–µ–¥—ã–¥—É—â–∏–π –≤—Ç–æ—Ä–æ–π –∞—Ä–≥—É–º–µ–Ω—Ç (b)
    # - –≤—Ç–æ—Ä–æ–π –∞—Ä–≥—É–º–µ–Ω—Ç: –æ—Å—Ç–∞—Ç–æ–∫ –æ—Ç –¥–µ–ª–µ–Ω–∏—è a –Ω–∞ b (a % b)
    # –≠—Ç–æ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É–µ—Ç –º–∞—Ç–µ–º–∞—Ç–∏—á–µ—Å–∫–æ–π —Ñ–æ—Ä–º—É–ª–µ –ù–û–î(a, b) = –ù–û–î(b, a mod b)
    return gcd(b, a % b)


