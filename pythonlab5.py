def show_mathematical_steps(a, b):
    """
    –ü–æ–∫–∞–∑—ã–≤–∞–µ—Ç –º–∞—Ç–µ–º–∞—Ç–∏—á–µ—Å–∫–∏–µ —à–∞–≥–∏ –∞–ª–≥–æ—Ä–∏—Ç–º–∞ –ï–≤–∫–ª–∏–¥–∞.
    
    Args:
        a (int): –ü–µ—Ä–≤–æ–µ —á–∏—Å–ª–æ
        b (int): –í—Ç–æ—Ä–æ–µ —á–∏—Å–ª–æ
    """
    print("\nüß© –ú–ê–¢–ï–ú–ê–¢–ò–ß–ï–°–ö–ò–ï –®–ê–ì–ò:")
    original_a, original_b = a, b
    steps = []
    
    while b != 0:
        remainder = a % b
        steps.append(f"–ù–û–î({a}, {b}) = –ù–û–î({b}, {remainder})")
        a, b = b, remainder
    
    for i, step in enumerate(steps, 1):
        print(f"   –®–∞–≥ {i}: {step}")
    
    print(f"   –§–∏–Ω–∞–ª—å–Ω—ã–π —à–∞–≥: –ù–û–î({a}, 0) = {a}")
    print(f"   ‚à¥ –ù–û–î({original_a}, {original_b}) = {a}")


